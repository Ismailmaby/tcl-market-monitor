def call_api(prompt, api_key):
    payload = json.dumps({
        "model": MODEL,
        "max_tokens": 4000,
        "messages": [{"role": "user", "content": prompt}]
    }).encode()

    req = urllib.request.Request(
        API_BASE + "/messages",
        data=payload,
        headers={
            "x-api-key": api_key,
            "anthropic-version": "2023-06-01",
            "content-type": "application/json",
        }
    )
    with urllib.request.urlopen(req, timeout=60) as resp:
        return json.loads(resp.read().decode())

def extract_sections(response):
    text = ""
    for block in response.get("content", []):
        if block.get("type") == "text":
            text += block.get("text", "")
    start = text.find("{")
    end = text.rfind("}") + 1
    if start == -1 or end == 0:
        print("  [WARN] No JSON found in response")
        print("  Preview: " + text[:300])
        return []
    try:
        data = json.loads(text[start:end])
        return data.get("sections", [])
    except Exception as e:
        print("  [WARN] JSON parse error: " + str(e)[:60])
        return []

def build_markdown(sections, run_date):
    lines = [
        "# TCL Hospitality Market Intelligence - Daily Briefing",
        "Date: " + run_date,
        "Markets: UAE, Saudi Arabia, Turkey, Brazil, Colombia, Peru, Panama, Chile",
        "Competing against: Samsung LYNK | LG ProCentric",
        "",
        "---",
        "",
    ]
    for s in sections:
        lines.append("## " + s.get("title", ""))
        for item in s.get("items", []):
            lines.append("### " + item.get("headline", ""))
            lines.append(item.get("insight", ""))
            lines.append("")
            if item.get("relevance"):
                lines.append("Relevance: " + item["relevance"])
            if item.get("action"):
                lines.append("Action: " + item["action"])
            lines.append("")
        lines.append("")
    lines.append("---")
    lines.append("Generated by TCL Market Monitor - " + run_date)
    return "\n".join(lines)

