def send_email(html_body, markdown_content, run_date):
    msg = MIMEMultipart("mixed")
    msg["Subject"] = "TCL Market Intel - " + run_date[:10]
    msg["From"]    = GMAIL_USER
    msg["To"]      = RECIPIENT
    msg.attach(MIMEText(html_body, "html"))
    attachment = MIMEText(markdown_content, "plain", "utf-8")
    attachment.add_header("Content-Disposition", "attachment",
                          filename="tcl_intel_" + run_date[:10] + ".md")
    msg.attach(attachment)
    with smtplib.SMTP_SSL("smtp.gmail.com", 465) as server:
        server.login(GMAIL_USER, GMAIL_PASS)
        server.sendmail(GMAIL_USER, RECIPIENT, msg.as_string())
    print("  Email sent to " + RECIPIENT)

def save_markdown(content, run_date):
    os.makedirs("output", exist_ok=True)
    path = "output/tcl_intel_" + run_date[:10] + ".md"
    with open(path, "w", encoding="utf-8") as f:
        f.write(content)
    print("  Markdown saved: " + path)

def main():
    run_date = datetime.datetime.utcnow().strftime("%Y-%m-%d %H:%M UTC")
    print("TCL Market Monitor - " + run_date)
    print("API: " + API_BASE + " | Model: " + MODEL)

    print("Calling API...")
    try:
        response = call_api(get_prompt(), ANTHROPIC_KEY)
        sections = extract_sections(response)
        print("Sections returned: " + str(len(sections)))
    except Exception as e:
        print("ERROR: " + str(e))
        sections = []

    total = sum(len(s.get("items", [])) for s in sections)
    print("Total insights: " + str(total))

    md   = build_markdown(sections, run_date)
    html = build_html_email(sections, run_date)
    save_markdown(md, run_date)
    send_email(html, md, run_date)
    print("Done.")

if __name__ == "__main__":
    main()
